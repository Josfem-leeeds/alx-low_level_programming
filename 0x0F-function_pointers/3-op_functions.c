#include "3-calc.h"

/**
 * op-add - add function
 * @a: first parameter
 * @b: second parameter
 * Return: returns the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op-sub - sub function
 * @a: first parameter
 * @b: second parameter
 * Return: returns the subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op-mul - multiplication function
 * @a: first parameter
 * @b: second parameter
 * Return: returns the multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op-div - div function
 * @a: first parameter
 * @b: second parameter
 * Return: returns the division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op-mod - add function
 * @a: first parameter
 * @b: second parameter
 * Return: returns the modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}

